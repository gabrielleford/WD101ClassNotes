/*
Literals represent value in JS.
Literals are the literal value we give our variables.
*/

// variable declaration
let potato;

// String literal
// keyword             value
   let potato = 'my favorite food.';
   //  variable

// Numeric literal
let x = 0;

// Boolean literal
let y = true;

// Object literal
let movie = {
    title: 'Elf',
    rating: 'PG',
    release: 'Nov 7th 2003'
}

// Array literal
let groceryList = ['apples', 'milk', 'bread', 'cheese'];
